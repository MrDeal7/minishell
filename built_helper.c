#include "minishell.h"

int	can_i_exit(char	*str)
{
	int	i;

	i = 0;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (ft_isdigit(str[i]) == 1)
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
